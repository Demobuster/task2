class Part {

    private int degree;
    private int value;

    public Part(int degree, int value) {
        this.degree = degree;
        this.value = value;
    }

    public int getDegree() {
        return degree;
    }

    public int getValue() {
        return value;
    }

    public void setDegree(int degree) {
        this.degree = degree;
    }

    public void setValue(int value) {
        this.value = value;
    }

}
